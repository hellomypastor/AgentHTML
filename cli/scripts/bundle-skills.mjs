import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const skillsDir = join(__dirname, '..', '..', 'skills', 'agenthtml');
const outFile = join(__dirname, '..', 'src', 'generated', 'skill-files.ts');

const files = {
  'SKILL.md': readFileSync(join(skillsDir, 'SKILL.md'), 'utf-8'),
  'references/agent-protocol.md': readFileSync(join(skillsDir, 'references', 'agent-protocol.md'), 'utf-8'),
  'references/style-presets.md': readFileSync(join(skillsDir, 'references', 'style-presets.md'), 'utf-8'),
};

const entries = Object.entries(files)
  .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)}`)
  .join(',\n');

const output = `// Auto-generated by scripts/bundle-skills.mjs — do not edit
export const SKILL_FILES: Record<string, string> = {\n${entries}\n};\n`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, output, 'utf-8');
console.log(`Bundled ${Object.keys(files).length} skill files into src/generated/skill-files.ts`);
